function printHello() {
  console.log( "Hello, World!");
}

// Now call above function after 2 seconds
setInterval(printHello, 2000);

//As a result, when you run this program, you will see "Hello, World!" printed to the console every 2 seconds, creating a repeating message with a 2-second interval between each printout. This is often used for tasks that need to be executed periodically, like updating information on a webpage or performing background tasks in a web application.

